    public static ArrayList<String> listaPuertos() {
        ArrayList<String> puertos = new ArrayList<String>();
        SerialPort[] comPort = SerialPort.getCommPorts();
        for (int i = 0; i < comPort.length; i++) {
            puertos.add(comPort[i].getSystemPortName());
        }
        return puertos;
    }

